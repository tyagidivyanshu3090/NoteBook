# Q: 1 Data type and there type function value:

a = 10
print(type(a)) # <class 'int'>  

b = 10.5
print(type(b)) # <class 'float'>

c = "Hello"
print(type(c)) # <class 'str'>

d = True
print(type(d)) # <class 'bool'>

e = None
print(type(e)) # <class 'NoneType'>

