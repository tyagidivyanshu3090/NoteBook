# Slicing the string


# Length - Negative Number = Positive Index.
fruit = "Pomogranate"
print(fruit[-5:-2]) # Output: "ana"    
# length - negative number = positive index 
    # 11 - (5) = 6 // I.e. start from a 
    # 11 - (2) = 9 // I.e. end at t not included

fruit = "Mango"
print(fruit[0:3]) # Output: "Man"
print(fruit[:4]) # Output: "Mang"
print(fruit[2:]) # Output: "ngo"
print(fruit[-2:]) # Output: "go"    // 5-2 = 3








